class Calculation:

    def __init__(self, num1, num2):
        self.num1 = num1
        self.num2 = num2

    def add(self):
        return self.num1 + self.num2

    def subtract(self):
        return self.num1 - self.num2

    def multiply(self):
        return self.num1 * self.num2

    def divide(self):
        return self.num1 / self.num2


calculate = Calculation(10, 2)

print(calculate.add())
print(calculate.subtract())
print(calculate.multiply())
print(calculate.divide())
